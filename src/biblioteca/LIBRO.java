    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String id_libro = txt_codigo.getText();
        String titulo = txt_titulo.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/YYYY");
        Date edic1 = txt_edicion.getDate();
        String edic = sdf.format(edic1);
        String ciudad = txt_ciudad.getText();
        int anio = Integer.parseInt(txt_año.getText());
        String editorial = txt_editorial.getText();
        String descripcion = txt_descripcion.getText();
        int valor_libro = Integer.parseInt(txt_valor.getText());
        String nombre_coleccion = (String) txt_coleccion.getSelectedItem();
        Listas.Consultas obtn = new Listas.Consultas();
        Lis_Coleccion = obtn.obtcoleccionc(nombre_coleccion);
        int id_coleccion = Lis_Coleccion.get(0).getId_coleccion();
        int val = 0;
        for (int i = 0; i < id_libro.length(); i++) {
            char a = id_libro.charAt(i);
            if (i < 2) {
                if (Character.isLetter(a)) {
                    val++;
                }
            } else {
                if (Character.isDigit(a)) {
                    val++;
                }
            }
        }
        if (val == 6) {
           
            String Consultasql = "INSERT INTO LIBRO (id_libro,titulo,edicion,ciudad,anio,editorial,descripcion,valor_libro,id_coleccion) VALUES (?,?,?,?,?,?,?,?,?)";
            try {
                
                PreparedStatement ps = cn.prepareStatement(Consultasql);
                ps.setString(1, id_libro);
                ps.setString(2, titulo);
                ps.setString(3, edic);
                ps.setString(4, ciudad);
                ps.setInt(5,anio);
                ps.setString(6, editorial);
                ps.setString(7,descripcion);
                ps.setInt(8, valor_libro);
                ps.setInt(9, id_coleccion);
                ps.execute();
               
                librosautor(id_libro);
                JOptionPane.showMessageDialog(null, "Libro registrado correctamente");
            } catch (Exception e) {
                System.out.println(""+e);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Codigo incorrecto");
        }


    }//GEN-LAST:event_jButton3ActionPerformed

