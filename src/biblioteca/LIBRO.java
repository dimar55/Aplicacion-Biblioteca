public void llenar_autor() {
        Listas.Consultas obtn = new Listas.Consultas();
        Lis_autor = obtn.obtautor();

        for (int i = 0; i < Lis_autor.size(); i++) {
            int id = Lis_autor.get(i).getId_autor();
            String nombre = Lis_autor.get(i).getNombre();
            Object datos[] = {id, nombre};
            mdAutor.addRow(datos);
        }

    }

    public void llenar_coleccion() {
        Listas.Consultas obtn = new Listas.Consultas();
        Lis_Coleccion = obtn.obtcoleccion();

        for (int i = 0; i < Lis_Coleccion.size(); i++) {
            String nombre = Lis_Coleccion.get(i).getNombre();
            txt_coleccion.addItem(nombre);
        }
        
    }
    
    public void librosautor(String id_libro){
        String Consultasql = "INSERT INTO LIBROSAUTOR(id_libro,id_autor) VALUES (?,?)";
        for (int i = 0; i < Idautor.size(); i++) {
            
        int id_autor = Integer.parseInt(Idautor.get(i));
        try {
            PreparedStatement ps = cn.prepareStatement(Consultasql);
            ps.setString(1, id_libro);
            ps.setInt(2, id_autor);
            ps.execute();
        } catch (Exception e) {
        }
    }
    }
